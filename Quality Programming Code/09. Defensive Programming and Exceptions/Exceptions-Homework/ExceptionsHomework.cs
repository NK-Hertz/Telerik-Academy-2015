using System;
using System.Collections.Generic;
using System.Text;

class ExceptionsHomework
{
    public static T[] Subsequence<T>(T[] arr, int startIndex, int count)
    {
        if (arr == null || arr.Length == 0)
        {
            return new T[0];
        }

        if (startIndex < 0 || arr.Length < startIndex)
        {
            throw new ArgumentOutOfRangeException("Start index must be between 0 and the length of the collection!");
        }

        if (count <= 0 || arr.Length < count)
        {
            throw new ArgumentOutOfRangeException("Count must be between 1 and the length of the collection!");
        }

        if (startIndex + count > arr.Length)
        {
            throw new ArgumentOutOfRangeException("The sum of start index and count must be smaller than or equal to the length of the collection!");
        }

        List<T> result = new List<T>();
        for (int i = startIndex; i < startIndex + count; i++)
        {
            result.Add(arr[i]);
        }
        return result.ToArray();
    }

    public static string ExtractEnding(string str, int count)
    {
        if (count <= 0 || str.Length < count)
        {
            throw new ArgumentOutOfRangeException("Count must be equal to or smaller than strings length!");
        }

        StringBuilder result = new StringBuilder();
        for (int i = str.Length - count; i < str.Length; i++)
        {
            result.Append(str[i]);
        }
        return result.ToString();
    }

    public static void CheckPrime(int number)
    {
        var isPrime = true;
        for (int divisor = 2; divisor <= Math.Sqrt(number); divisor++)
        {
            if (number % divisor == 0)
            {
                isPrime = false;
                
                break;
            }
        }

        if (isPrime)
        {
            Console.WriteLine("{0} is prime!", number);
        }
        else
        {
            Console.WriteLine("{0} is not prime!", number);
        }
    }

